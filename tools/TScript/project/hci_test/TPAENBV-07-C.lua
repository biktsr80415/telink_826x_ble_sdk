tl_usb_bulk_monitor_start(handle_bulk)

--Mask event: LE Generate DHkey Complete, LE Read Local P256 Public Key Complete
hci_le_set_event_mask(BLE_SUCCESS, 0x80,0x01,0x00,0x00,0x00,0x00,0x00,0x00) 

hci_le_read_local_p256_pkey(BLE_SUCCESS)

